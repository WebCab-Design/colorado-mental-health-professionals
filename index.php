			<div class="tabs-panel" id="panel2">
				<p>Springs Recovery Connection, Phoenix MultiSport and the TSC Care Co-op are partnering together hold The Springs Recovery Rally and 5K Run/3K Walk on Saturday, September 10th from 9:00 AM to 2:00 PM at America the Beautiful Park, in downtown Colorado Springs.</p>

				<p>This event will celebrate our successes as a recovery community, put a face and voice to recovery from substance abuse disorder, and reduce the stigma that so often surrounds the disease of addiction. We believe Colorado Springs is a great place to recover from substance use disorders and find freedom from addiction!</p>

				<p>Join us at 9:00 AM for our 5K Run/3K Walk for Recovery, sponsored by Phoenix MultiSport. Over 200 runners are expected to participate. At 10:00 the rally begins as we celebrate National Recovery Month together. We will have live music, speakers, games and contests, a “kids zone” full of activities and prizes, great food and much more!  Representatives from healthcare organizations, in-patient and out-patient recovery centers, 12-step groups, non-profit organizations, and others committed to proving help to people seeking recovery and their families, will be onsite to share resources and hope. </p>

				<p>Event sponsorship opportunities, race registration and additional information can be found at: <a href="http://springsrecoveryrally.org">springsrecoveryrally.org</a>. You may also call Cathy Plush for more info at: <a href="tel:7193673292">719-367-3292</a> or e-mail us at: <a href="mailto:info@springsrecoveryconnection.org">info@springsrecoveryconnection.org</a>.</p>

				<p><strong><a href="http://www.springsrecoveryconnection.org/" target="new">Springs Recovery Connection</a></strong> <blockquote>Is a non-profit organization serving the community of Colorado Springs and surrounding areas. We offer a wide variety of services including information and referral, substance abuse awareness education, Recovery Coaching, sober activities and support group meetings. <a href="http://www.springsrecoveryconnection.org/" target="new">www.springsrecoveryconnection.org.</a></p></blockquote>

				<p><strong><a href="http://www.phoenixmultisport.org/" target="new">Phoenix MultiSport</a></strong> <blockquote>Fosters a supportive, physically active community for individuals who are recovering from a substance use disorder and those who choose to live sober. Through pursuits such as climbing, hiking, running, strength training, yoga, road/mountain biking, socials and other activities, we seek to help our members develop and maintain the emotional strength they need to stay sober. <a href="http://www.phoenixmultisport.org" target="new">www.phoenixmultisport.org.</a></p></blockquote>

				<p><strong><a href="http://www.tsccareco-op.org/" target="new">TSC Care Co-op</a></strong> <blockquote>Is a partnership of organizations in who provide accessible mental health and recovery care for people in Colorado Springs. As a group we provide donation-based individual, couples, youth, marriage and family counseling; secular and faith-based recovery resources groups and resources; and, are training the next generation of Licensed Professional Counselors and Certified/Licensed Addictions counselors in the State of Colorado. The TSC Care Co-op is a community-based resources, founded by The Sanctuary Church, a “recovery” church on the West Side. <a href="http://www.tsccareco-op.org/" target="new">www.tsccareco-op.org.</a></p></blockquote>
